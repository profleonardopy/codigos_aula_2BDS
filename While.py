# Inicializamos a variável 'numero' com o valor 0
numero = 0

# Iniciamos um loop while que irá continuar enquanto 'numero' for menor a 5
while numero < 5:
    # Imprimimos o valor atual de 'numero'
    print(numero)
    # Incrementamos o valor de 'numero' em 1 para a próxima iteração
    numero += 1
print("Terminou!")